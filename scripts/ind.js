let makeupArr = []
function showmakeup(){
    let makeup = document.getElementById('makeupfilter').value

    for(let i=0 ; i<prodArr.length ; i++)
    {
        if(makeup=="face"){
            makeupArr= prodArr.filter(function(prod){
              return(prod.category=='Face')  
            });
            
        }

        if(makeup=="lips"){
            makeupArr= prodArr.filter(function(prod){
              return(prod.category=='Lips')  
            });
        }

        if(makeup=="eyes"){
            makeupArr= prodArr.filter(function(prod){
              return(prod.category=='Eyes')  
            });
        }
        
    }

    
    localStorage.setItem("ProductPage",JSON.stringify(makeupArr))
    
    window.location.href='product.html'
}


